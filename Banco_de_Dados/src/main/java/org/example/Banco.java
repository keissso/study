package org.example;

// This class is used to store the user's input and key
public class Banco {
    private String input;
    private String Key;

    public Banco() {
    }

    public String getInput() {
        return input;
    }

    public void setInput(String input) {
        this.input = input;
    }

    public String getKey() {
        return Key;
    }

    public void setKey(String key) {
        Key = key;
    }
}


